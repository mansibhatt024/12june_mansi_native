//wap  to find number is even or odd using ternary operator
#include <stdio.h>  
  
int main()  
{  
    int a;  
  
    printf("Enter the value of no.");  
    
    scanf("%d", &a);  
  
   if(a % 2 == 0)   
    {
    	printf("no. is even");
	}
	else {
		printf("no. is  odd");
	}
  
    return 0;  
}  
